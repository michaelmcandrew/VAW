/**
 * Set up themeable content types.
 * Template files in the form page-node-your_content_type.tpl.php
 */

function pauls_theme_preprocess_page(&$variables) {
  if ($variables['node']->type != "") {
    $variables['template_files'][] = "page-node-" . $variables['node']->type;
  }
}


